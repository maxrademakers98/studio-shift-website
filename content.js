// ─── Injector — do not edit below this line ────────
(function injectContent() {
  // Plain text / HTML elements
  document.querySelectorAll('[data-content]').forEach(el => {
    const key = el.getAttribute('data-content');
    if (CONTENT[key] === undefined) return;
    if (el.namespaceURI === 'http://www.w3.org/2000/svg') {
      el.textContent = CONTENT[key];
    } else {
      el.innerHTML = CONTENT[key];
    }
  });

  // Chip arrays (with optional sub-group labels)
  document.querySelectorAll('[data-chips]').forEach(el => {
    const chips = CONTENT[el.getAttribute('data-chips')];
    if (!chips) return;
    el.innerHTML = chips.map(item =>
      typeof item === 'object' && item.sub
        ? `<span class="step-chip-sub">${item.sub}</span>`
        : `<div class="step-chip">${item.chip || item}</div>`
    ).join('');
  });

  // Tag arrays
  document.querySelectorAll('[data-tags]').forEach(el => {
    const tags = CONTENT[el.getAttribute('data-tags')];
    if (!tags) return;
    el.innerHTML = tags.map(t => `<span class="team-tag">${t}</span>`).join('');
  });

  // How-we-work pills
  document.querySelectorAll('[data-how-pills]').forEach(el => {
    const pills = CONTENT[el.getAttribute('data-how-pills')];
    if (!pills) return;
    el.innerHTML = pills.map(t => `<span class="how-pill">${t}</span>`).join('');
  });

  // LinkedIn links (hidden when empty)
  document.querySelectorAll('[data-linkedin]').forEach(el => {
    const url = CONTENT[el.getAttribute('data-linkedin')];
    if (url) el.href = url;
  });

  // Ticker strips (items duplicated for seamless CSS loop)
  document.querySelectorAll('[data-ticker]').forEach(el => {
    const items = CONTENT[el.getAttribute('data-ticker')];
    if (!items) return;
    const html = items.map(t => `<span class="ticker-item">${t}</span>`).join('');
    el.innerHTML = html + html;
  });
})();
